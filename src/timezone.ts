/**
 * Check if a timezone is supported by the Temporal API
 */
export const isTimeZoneSupported = (timezone: string) => {
	try {
		return Temporal.TimeZone.from(timezone).id !== null;
	} catch (error) {
		return false;
	}
};

/**
 * Get the timezone of the device
 * @returns The device timezone or null if not supported
 */
export const getDeviceTimezone = (): string | null => {
	if (
		// CanIUse suggests we can use this for all recent browsers / mobile browsers, but this doesn't work in IE.
		typeof Intl !== "undefined" &&
		Intl?.DateTimeFormat?.()?.resolvedOptions?.()?.timeZone
	) {
		return Intl.DateTimeFormat().resolvedOptions().timeZone;
	}
	return null;
};
